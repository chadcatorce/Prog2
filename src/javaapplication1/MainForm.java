    private String getLastEmployeeNo() throws Exception {
        String lastEmployeeNo = "";
        
        String url = "jdbc:mysql://localhost:3306/your-database-name"; // Adjust as necessary
        String user = "your-username"; // Adjust as necessary
        String password = "your-password"; // Adjust as necessary

        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;
        
        try {
            // Establish the database connection
                conn = DriverManager.getConnection(url, user, password);
            // Create a statement
            stmt = conn.createStatement();
            // Execute the query
            rs = stmt.executeQuery("SELECT id FROM employees ORDER BY id DESC LIMIT 1");
            // Process the result set
            if (rs.next()) {
                lastEmployeeNo = rs.getString("id");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            throw e; // Rethrow the exception for further handling
        } finally {
            // Close resources
            if (rs != null) {
                try {
                    rs.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            if (stmt != null) {
                try {
                    stmt.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
        return lastEmployeeNo;
    }
        
    private void displayEmployeesData() {
        List<Employee> employees = database.getAllEmployees();

        String[] columnNames = {"ID", "First Name", "Last Name", "SSS No.", "Philhealth No.", "TIN", "Pagibig No."};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        for (Employee emp : employees) {
            Object[] rowData = {
                emp.getId(),
                emp.getFirst_name(),
                emp.getLast_name(),
                emp.getSss(),
                emp.getPhilhealth(),
                emp.getTin(),
                emp.getPagibig(),
            };
            model.addRow(rowData);
        }
        tblEmployees.setModel(model);
        
        tblEmployees.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = tblEmployees.getSelectedRow();
                if (selectedRow != -1) {
                    updateFields(true);
                    // If a row is selected
                    DefaultTableModel model1 = (DefaultTableModel) tblEmployees.getModel();
                    // Retrieve data from the selected row
                    String employeeNo = model1.getValueAt(selectedRow, 0).toString();
                    String firstName = model1.getValueAt(selectedRow, 1).toString();
                    String lastName = model1.getValueAt(selectedRow, 2).toString();
                    String sss = model1.getValueAt(selectedRow, 3).toString();
                    String philhealth = model1.getValueAt(selectedRow, 4).toString();
                    String tin = model1.getValueAt(selectedRow, 5).toString();
                    String pagibig = model1.getValueAt(selectedRow, 6).toString();
                    // Set data to the fields
                    lblEmployeeNo.setText(employeeNo);
                    txtFirstName.setText(firstName);
                    txtLastName.setText(lastName);
                    txtSSS.setText(sss);
                    txtPhilhealth.setText(philhealth);
                    txtTIN.setText(tin);
                    txtPagibig.setText(pagibig);
                } else {
                    updateFields(false);
                }
            }
        });
    }
    
