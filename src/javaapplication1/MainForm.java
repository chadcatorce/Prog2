    private void btnUpdateRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateRecordActionPerformed
        String lastName = txtLastName.getText();
        String firstName = txtFirstName.getText();
        String sss = txtSSS.getText();
        String philhealth = txtPhilhealth.getText();
        String tin = txtTIN.getText();
        String pagibig = txtPagibig.getText();
        String employeeNo = lblEmployeeNo.getText();

        // Call the update functionality in db.java
        boolean success = database.updateRecord(lastName, firstName, sss, philhealth, tin, pagibig, employeeNo);
        
        // Check if the update was successful
        if (success) {
            displayEmployeesData();
            clearFields();
            JOptionPane.showMessageDialog(MainForm.this, "Record updated successfully!");
        } else {
            JOptionPane.showMessageDialog(MainForm.this, "Failed to update record. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateRecordActionPerformed

